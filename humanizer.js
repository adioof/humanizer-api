class LLMProvider {
  constructor(config) {
    this.provider = config.provider || 'openai';
    this.apiKey = config.apiKey;
    this.model = config.model;
    this.baseUrl = config.baseUrl;

    if (this.provider === 'openai') {
      this.model = this.model || 'gpt-4o-mini';
      this.baseUrl = this.baseUrl || 'https://api.openai.com';
    } else if (this.provider === 'anthropic') {
      this.model = this.model || 'claude-sonnet-4-20250514';
      this.baseUrl = this.baseUrl || 'https://api.anthropic.com';
    } else if (this.provider === 'custom') {
      if (!this.baseUrl) throw new Error('baseUrl required for custom provider');
      this.model = this.model || 'default';
    }
  }

  async complete(systemPrompt, userPrompt, temperature = 0.8) {
    if (this.provider === 'anthropic') {
      return this._anthropic(systemPrompt, userPrompt, temperature);
    }
    return this._openai(systemPrompt, userPrompt, temperature);
  }

  _openai(systemPrompt, userPrompt, temperature) {
    const body = JSON.stringify({
      model: this.model,
      messages: [
        { role: 'system', content: systemPrompt },
        { role: 'user', content: userPrompt }
      ],
      temperature,
      max_tokens: 4096,
    });

    const url = new URL(this.baseUrl + '/v1/chat/completions');
    return this._request(url, body, {
      'Authorization': `Bearer ${this.apiKey}`,
      'Content-Type': 'application/json',
    }).then(data => {
      const parsed = JSON.parse(data);
      if (parsed.error) throw new Error(parsed.error.message || JSON.stringify(parsed.error));
      return parsed.choices?.[0]?.message?.content || '';
    });
  }

  _anthropic(systemPrompt, userPrompt, temperature) {
    const body = JSON.stringify({
      model: this.model,
      max_tokens: 4096,
      system: systemPrompt,
      messages: [{ role: 'user', content: userPrompt }],
      temperature,
    });

    const url = new URL('https://api.anthropic.com/v1/messages');
    return this._request(url, body, {
      'x-api-key': this.apiKey,
      'anthropic-version': '2023-06-01',
      'Content-Type': 'application/json',
    }).then(data => {
      const parsed = JSON.parse(data);
      if (parsed.error) throw new Error(parsed.error.message || JSON.stringify(parsed.error));
      return parsed.content?.[0]?.text || '';
    });
  }

  _request(url, body, headers) {
    return new Promise((resolve, reject) => {
      const mod = url.protocol === 'https:' ? https : http;
      const req = mod.request(url, {
        method: 'POST',
        headers: { ...headers, 'Content-Length': Buffer.byteLength(body) },
        timeout: 120000,
      }, res => {
        let data = '';
        res.on('data', c => data += c);
        res.on('end', () => {
          if (res.statusCode >= 400) {
            reject(new Error(`LLM API error ${res.statusCode}: ${data.slice(0, 500)}`));
          } else {
            resolve(data);
          }
        });
      });
      req.on('error', reject);
      req.on('timeout', () => { req.destroy(); reject(new Error('LLM request timeout')); });
      req.write(body);
      req.end();
    });
  }
}

