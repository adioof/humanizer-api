class AIDetector {
  constructor(config = {}) {
    this.provider = config.provider || 'gptzero';
    this.apiKey = config.apiKey;
    this.model = config.model;
  }

  async detect(text) {
    if (this.provider === 'inhouse') return this._inhouse(text);

    if (!this.apiKey) {
      return { score: 0, human_score: 1, sentences: [], skipped: true };
    }

    if (this.provider === 'gptzero') return this._gptzero(text);
    if (this.provider === 'sapling') return this._sapling(text);
    if (this.provider === 'inhouse') return this._inhouse(text);
    throw new Error(`Unknown detector provider: ${this.provider}`);
  }

  async _inhouse(text) {
    // Uses the in-house detector (OpenAI logprobs + perplexity/burstiness)
    const { detect } = require('./detector');
    const result = await detect(text, this.apiKey, { model: this.model || 'anthropic/claude-opus-4-6' });
    return {
      provider: 'inhouse',
      score: (100 - result.score) / 100, // convert: detector gives 0-100 human, we need 0-1 AI
      human_score: result.score / 100,
      sentences: (result.flagged_sentences || []).map(s => ({
        text: s.text,
        prob: 1 - (s.perplexity / 60), // rough conversion
      })),
      metrics: result.metrics,
    };
  }

  _gptzero(text) {
    const body = JSON.stringify({ document: text });
    const url = new URL('https://api.gptzero.me/v2/predict/text');

    return this._request(url, body, {
      'x-api-key': this.apiKey,
      'Content-Type': 'application/json',
    }).then(data => {
      const parsed = JSON.parse(data);
      const doc = parsed.documents?.[0] || {};
      return {
        provider: 'gptzero',
        score: doc.completely_generated_prob || 0,
        human_score: 1 - (doc.completely_generated_prob || 0),
        sentences: (doc.sentences || [])
          .filter(s => s.generated_prob > 0.7)
          .map(s => ({ text: s.sentence, prob: s.generated_prob })),
      };
    });
  }

  _sapling(text) {
    const body = JSON.stringify({ text });
    const url = new URL('https://api.sapling.ai/api/v1/aidetect');

    return this._request(url, body, {
      'Content-Type': 'application/json',
    }).then(data => {
      const parsed = JSON.parse(data);
      return {
        provider: 'sapling',
        score: parsed.score || 0,
        human_score: 1 - (parsed.score || 0),
        sentences: (parsed.sentence_scores || [])
          .filter(s => s.score > 0.7)
          .map(s => ({ text: s.sentence, prob: s.score })),
      };
    });
  }

  _request(url, body, headers) {
    return new Promise((resolve, reject) => {
      const req = https.request(url, {
        method: 'POST',
        headers: { ...headers, 'Content-Length': Buffer.byteLength(body) },
        timeout: 30000,
      }, res => {
        let data = '';
        res.on('data', c => data += c);
        res.on('end', () => {
          if (res.statusCode >= 400) {
            reject(new Error(`Detector API error ${res.statusCode}: ${data.slice(0, 300)}`));
          } else {
            resolve(data);
          }
        });
      });
      req.on('error', reject);
      req.on('timeout', () => { req.destroy(); reject(new Error('Detector timeout')); });
      req.write(body);
      req.end();
    });
  }
}

